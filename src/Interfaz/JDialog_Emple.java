    private void btn_AceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AceptarActionPerformed
        Validar v = new Validar();
        if (!v.campoVacio(txt_RutEmpleado.getText()) && !v.campoVacio(txt_NombreEmpleado.getText()) && !v.campoVacio(txt_ApellidoPEmpleado.getText()) && !v.campoVacio(txtpass_Contraseña1Empleado.getText()) && !v.campoVacio(txtpass_Contraseña2Empleado.getText())) {
            if (añadir) 
                añadirEmpleado();
            else if (modificar) 
                modificarEmpleado(); 
        }
        else
            JOptionPane.showMessageDialog(null, "Los campos no pueden quedar vacíos.","Cuidado!",JOptionPane.WARNING_MESSAGE);
             
    }//GEN-LAST:event_btn_AceptarActionPerformed

    private void txt_NombreEmpleadoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_NombreEmpleadoKeyTyped
        Validar v = new Validar();       
        if (!v.maximoLargo(txt_NombreEmpleado.getText(), 20))
            evt.consume();

        if(Character.isDigit(evt.getKeyChar())) 
            evt.consume(); 
    }//GEN-LAST:event_txt_NombreEmpleadoKeyTyped

