    private void añadirEmpleado() {
        Empleado emp;
        Validar v = new Validar();
        if (txtpass_Contraseña1Empleado.getText().equals(txtpass_Contraseña2Empleado.getText())) {

                rut = v.validarRut(txt_RutEmpleado.getText());

                if (rut == null)
                    JOptionPane.showMessageDialog(null, "Rur inválido, favor revísarlo.","Precaución.",JOptionPane.WARNING_MESSAGE);

                else{
                    emp = cargarDatos();

                    if(emp.insertarEmpleado()){
                        JOptionPane.showMessageDialog(null, "El cliente "+emp.getNombre()+" fue añadido correctamente.","Cliente ingresado",JOptionPane.INFORMATION_MESSAGE);
                        this.lbl_CodigoEmpleado.setText(String.valueOf(Integer.parseInt(this.lbl_CodigoEmpleado.getText()) + 1)); //Actualiza el lblId
                        limpiar();
                    }
                }
            }
            else
                JOptionPane.showMessageDialog(null, "Contraseñas no coinciden!.","Favor verifica la contraseña.",JOptionPane.WARNING_MESSAGE);
    }

    private void modificarEmpleado() {
        Empleado emp = cargarDatos();

        if (emp.actualizarEmpleado())
            JOptionPane.showMessageDialog(null, "Datos editados correctamente.","Datos empleado editados",JOptionPane.INFORMATION_MESSAGE);
    }
