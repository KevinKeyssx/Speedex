    private Empleado cargarDatos(){
        Empleado emp = new Empleado();

        emp.setIdEmpleado(lbl_CodigoEmpleado.getText());
        emp.setNombre(this.txt_NombreEmpleado.getText());
        emp.setApellidoP(this.txt_ApellidoPEmpleado.getText());
        emp.setApellidoM(this.txt_ApellidoMEmpleado.getText());
        emp.setRut(rut);
        emp.setContraseña(this.txtpass_Contraseña1Empleado.getText());
        emp.setTipo(cmb_TipoEmpleado.getSelectedItem().toString());
        
        return emp;
    }
    
    private void iniciarComponentes(){
        txt_NombreEmpleado.requestFocus();
        
        if (añadir)
            setTitle("Agregar Empleado - SpeedeX");
        else
            setTitle("Modificar Empleado - SpeedeX");

        this.setLocationRelativeTo(null); //Centrar el JFrame al centro de la pantalla
        setIconImage(new ImageIcon(getClass().getResource("/Complementos/Logo Speedex2.png")).getImage()); // Icono del programa
        this.setResizable(false);
        setSize(824, 510); 
        
        lbl_Ingresar.setVisible(false);
        lbl_Modificar.setVisible(false);
        
        cmb_TipoEmpleado.removeAllItems();
        cmb_TipoEmpleado.addItem("Empleado");
        cmb_TipoEmpleado.addItem("Administrador");
        cmb_TipoEmpleado.addItem("Inhabilitado");
    
    }

    public JDialog_Emple(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        initComponents();
        
        añadir = true;
        iniciarComponentes();
        
        Empleado emp = new Empleado();
        
        lbl_Ingresar.setVisible(true);
        lbl_CodigoEmpleado.setText(String.valueOf(Integer.parseInt(emp.obtenerCodigo().getIdEmpleado()) + 1)); //Obtiene el codigo y le suma 1
    }
    
    public JDialog_Emple(java.awt.Frame parent, boolean modal, Empleado emp) {
        super(parent, modal);
        initComponents();
        
        iniciarComponentes();
        
        Empleado Emp = emp;
        
        modificar = true;
        
        lbl_CodigoEmpleado.setText(Emp.getIdEmpleado());
        txt_NombreEmpleado.setText(Emp.getNombre());
        txt_ApellidoPEmpleado.setText(Emp.getApellidoP());
        txt_ApellidoMEmpleado.setText(Emp.getApellidoM());
        txt_RutEmpleado.setText(Emp.getRut());
        
        int tipo = 0;
                
        if (Emp.getTipo().equals("Administrador")) 
            tipo = 1;
        else if (Emp.getTipo().equals("Inhabilitado"))
            tipo = 2;
        
        cmb_TipoEmpleado.setSelectedIndex(tipo);
        
        lbl_Modificar.setVisible(true);
        
        txt_RutEmpleado.setEditable(false);
    }

